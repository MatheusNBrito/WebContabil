// üîπ Rota para upload de arquivos (clientes podem enviar arquivos)
router.post("/upload", upload.single("file"), async (req, res) => {
    try {
        // üîπ Temporariamente: userId √© enviado pelo frontend (N√ÉO SEGURO!)
        const { userId } = req.body;

        if (!userId) {
            return res.status(400).json({ error: "userId √© obrigat√≥rio." });
        }

        // Verificar se o usu√°rio existe
        const user = await User.findById(userId);
        if (!user) {
            return res.status(404).json({ error: "Usu√°rio n√£o encontrado." });
        }

        // Criar arquivo (vinculado ao userId fornecido)
        const file = await File.create({
            filename: req.file.originalname,
            path: req.file.path,
            mimetype: req.file.mimetype,
            size: req.file.size,
            uploadedBy: userId,
            assignedTo: userId, // Assume que o arquivo √© para o pr√≥prio usu√°rio
        });

        return res.json({ message: "‚úÖ Arquivo enviado!", file });

    } catch (error) {
        console.error("Erro:", error);
        return res.status(500).json({ error: "Erro no servidor." });
    }
});

