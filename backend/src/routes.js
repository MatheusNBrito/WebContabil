router.delete("/files/:fileId", authenticate, async (req, res) => {
    console.log(`üì¢ Rota /files/${req.params.fileId} foi chamada!`);

    try {
        const { fileId } = req.params;
        const userId = req.user.id; // ID do usu√°rio logado (do token JWT)
        const userRole = req.user.role; // Role do usu√°rio logado

        // üîπ Buscar o arquivo no banco de dados
        const file = await File.findById(fileId);
        if (!file) {
            return res.status(404).json({ error: "Arquivo n√£o encontrado." });
        }

        // üîπ Permitir exclus√£o apenas para quem enviou o arquivo OU para admin
        if (file.uploadedBy.toString() !== userId && userRole !== "admin") {
            return res.status(403).json({ error: "Voc√™ n√£o tem permiss√£o para excluir este arquivo." });
        }

        // üîπ Remover o arquivo do servidor
        const filePath = file.path;
        if (fs.existsSync(filePath)) {
            fs.unlinkSync(filePath);
            console.log("üìÇ Arquivo deletado do servidor:", filePath);
        }

        // üîπ Remover o arquivo do banco de dados
        await File.findByIdAndDelete(fileId);

        return res.json({ message: "‚úÖ Arquivo exclu√≠do com sucesso!" });
    } catch (error) {
        console.error("‚ùå Erro ao excluir arquivo:", error);
        return res.status(500).json({ error: "Erro ao excluir arquivo." });
    }
});

